            String brand = request.getParameter("brand");
            String displacement = request.getParameter("displacement");
            String car = request.getParameter("car");
            out.println("<p>Model: " + model + "</p>");
            out.println("<p>Brand: " + brand + "</p>");
            out.println("<p>Car engine displacement: " + displacement + "</p>");
            out.println("<p>Year of issue: " + car + "</p>");
            out.println("</div>\n"
                    + "</body>");
            out.println("</html>");

